int	ft_str_is_alpha(char *str)
{
	int		i;
	char	curr;

	i = 0;
	if (str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		curr = str[i];
		if (!((curr >= 65 && curr <= 90) || (curr >= 97 && curr <= 122)))
			return (0);
		str++;
	}
	return (1);
}	
